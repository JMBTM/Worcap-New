    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String data = password.getText();
        String result = "";
        MessageDigest md;
        try{
            md = MessageDigest.getInstance("SHA-512");
            
            md.update(data.getBytes());
            byte[] mb = md.digest();
            result = "";
            for(int i = 0; i <mb.length; i++)
            {
                byte temp = mb[i];
                String s = Integer.toHexString(new Byte(temp));
                while(s.length() < 2)
                {
                    s = "0" + s;
                }
                s = s.substring(s.length() - 2);
                result += s;
            }
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            PreparedStatement insert = con.prepareStatement("INSERT INTO UserLogin VALUES(?, ?, ?, ?)");
            insert.setInt(1, 0);
            insert.setString(2, email.getText());
            insert.setString(3, login.getText());
            insert.setString(4, result);
            insert.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(registerUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);
        try {
            new worcapClient().setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(registerUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

